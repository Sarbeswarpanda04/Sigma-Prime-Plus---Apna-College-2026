import java.util.*;
public class SpiralMatrix {
    
    public static void main(String[] args) {
        int n = 3, m = 4;
        int arr[][] = new int[n][m];

        //input
        Scanner sc =new Scanner(System.in);
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                arr[i][j] = sc.nextInt();
            }
        }

        

    }
}

#include<iostream>
using namespace std;
int main(){
    //Arithmatic Operators
    int a = 10;
    int b = 5;
    //addition
    cout<<"Addition = "<<a+b<<endl;

    //substraction
    cout<<"Substraction = "<<a-b<<endl;

    //Multiplication
    cout<<"Multiplication = "<<a*b<<endl;

    //Division
    cout<<"Division = "<<a/b<<endl;

    //Modulo
    cout<<"Modulo = "<<a%b<<endl;

    return 0;
}